import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve } from "node:path";

const filePath = resolve(process.cwd(), "build-meta.ts");

let current = 0;
if (existsSync(filePath)) {
	const content = readFileSync(filePath, "utf8");
	const match = content.match(/BUILD_NUMBER\s*=\s*(\d+)/);
	if (match) {
		current = Number.parseInt(match[1], 10) || 0;
	}
}

const next = current + 1;
const timestamp = new Date().toISOString();
const marker = `build-${next}`;

const nextContent = `// Auto-generated by scripts/bump-build-meta.mjs
export const BUILD_NUMBER = ${next};
export const BUILD_TIMESTAMP = "${timestamp}";
export const BUILD_MARKER = "${marker}";
`;

writeFileSync(filePath, nextContent, "utf8");
